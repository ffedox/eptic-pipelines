<h3>Documentation</h3>

<p>The European Parliament Translation and Interpreting Corpus (EPTIC)
    is developed at the University of Bologna <a
    href='?section=about'>and a few other universities</a> responsible
    for different language components. At present EPTIC comprises
    texts in <strong>English, Italian and French</strong>; however,
    other language components, including Finnish, Polish and Slovene
    are currently on the way.</p>

    <p>The corpus is currently <a
    href="https://corpora.dipintra.it/public/eptic.cgi/first_form">made
    available</a> through the NoSketch Engine platform (NoSke; Rychlý
    2007).</p>

<p>EPTIC is an <strong>intermodal and parallel corpus</strong> of a
    complex structure. Data included in the corpus is derived from the
    <a
    href='http://www.europarl.europa.eu/plenary/en/home.html'>official
    website</a> of the European Parliament, which provides videos and
    verbatim reports of the plenary sessions together with the
    interpretations of the speeches, as well as their translations
    (the latter only for plenaries which took place until
    mid-2011). Subcorpora are aligned to each other at sentence level,
    and transcripts of speeches and interpretations are time-aligned
    with the corresponding videos.</p>

<p>Each language combination component in EPTIC includes the following <strong>subcorpora</strong>:</p>

<ul>
    <li>sources – spoken: orthographic transcripts of the original speeches;</li>
    <li>sources – written: official verbatim reports of the source speeches;</li>
    <li>targets – interpreted: transcripts of the interpretations;</li>
    <li>targets – translated: translations of the verbatim reports.</li>
</ul>

<p>EPTIC is also a <strong>multilingual corpus</strong>, which
    includes several language combinations and
    translation/interpreting directions. All language combinations
    feature English as one of the languages involved. The subcorpora
    completed so far are: English&gt;French, French&gt;English,
    English&gt;Italian, Italian&gt;English, Polish&gt;English.</p>

