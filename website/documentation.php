    using the <strong>contextual information</strong> that allow to narrow down the
    query to, e.g., a speech delivered by a particular speaker, or a
    speech of particular length. The filtering options are described
    in the following Table.</p>
 
<p>As the corpus is aligned at sentence level, it is possible to
