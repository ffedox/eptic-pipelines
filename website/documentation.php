<h4>Characteristics of EPTIC texts</h4>

<p>EPTIC is comprised mostly of <strong>short speeches</strong>
 ranging from 100 to 300 words, but there are also medium and long
 speeches (the latter exceeding 1000 words). Speeches are delivered at
 the European Parliament plenary sessions by different speakers,
 including mostly <strong>MEPs</strong>, but also commissioners and
 guests. They are devoted to a range of <strong>topics</strong> (see
 Table below). The composition of the individual subcorpora with respect
 to the topics covered in the debates can be inferred by clicking on
 the <strong>Corpus info</strong> link within the <a
 href='https://corpora.dipintra.it/public/eptic.cgi/first_form'>NoSke
 interface</a>: in the <i>Structures and attributes</i> section,
 simply click on <i>Topic</i>.

