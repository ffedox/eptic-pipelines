	  <p>The <strong>European Parliament Translation and
    Interpreting Corpus (EPTIC)</strong> is an intermodal parallel
    corpus comprised of speeches delivered at the European Parliament,
    as well as their official interpretations and translations. The
    corpus has been compiled based on data collected from the <a
    href='http://www.europarl.europa.eu/plenary/en/home.html'
    target='_blank'>official website</a> of the European Parliament,
    where verbatim reports and videos of the speeches with
    multilingual audio tracks are made publicly available (until 2011
    together with the official translations). Sub-corpora are aligned
    to each other at sentence level, and transcripts of speeches
    and interpretations are time-aligned with the corresponding
    videos.</p>
            
            <p>Due to <strong>its unique structure</strong>, EPTIC
            allows its users to compare:</p>
	    <ul>
		<li>interpretations and translations (in various languages)</li>
            	<li>interpreted or translated language with
            	non-interpreted or non-translated language</li>
		<li>native English with non-native English</li>
		<li>orthographic transcripts with video recordings</li>
	    </ul>

            <p>EPTIC is a <a
                href='?section=about'>collaborative effort</a>
                of a team of researchers led by the University of
                Bologna. At present EPTIC includes texts in
                <strong>English, French and Italian</strong>, but a
                few other European languages will soon enrich the
                collection (among them Finnish, Polish and
                Slovene). The corpus can be accessed via NoSketch
                Engine (Rychl√Ω 2007), which is an open source corpus
                management system allowing multiple alignments.</p>
 
            <p>For more information on the corpus, see the <a href='?section=documentation'>documentation section</a>.</p>            
